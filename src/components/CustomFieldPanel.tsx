import { useDraggable } from '@dnd-kit/core';
import { FieldType } from '../types';

interface FieldItemProps {
  type: FieldType;
  label: string;
  icon?: React.ReactNode;
}

export const FieldItem = ({ type, label, icon }: FieldItemProps) => {
  const { attributes, listeners, setNodeRef, transform } = useDraggable({
    id: `draggable-${type}`,
    data: {
      type,
    },
  });

  const style = transform
    ? {
        transform: `translate3d(${transform.x}px, ${transform.y}px, 0)`,
      }
    : undefined;

  return (
    <div
      ref={setNodeRef}
      style={style}
      {...listeners}
      {...attributes}
      className="flex items-center p-2 my-2 bg-white rounded-md border border-gray-200 cursor-grab hover:bg-gray-50"
    >
      <div className="w-5 h-5 mr-2 text-gray-500">
        {icon}
      </div>
      <span className="text-sm">{label}</span>
      <div className="ml-auto text-gray-400">
        <svg width="16" height="16" viewBox="0 0 24 24" fill="none" xmlns="http://www.w3.org/2000/svg">
          <path d="M5 10C3.9 10 3 10.9 3 12C3 13.1 3.9 14 5 14C6.1 14 7 13.1 7 12C7 10.9 6.1 10 5 10ZM19 10C17.9 10 17 10.9 17 12C17 13.1 17.9 14 19 14C20.1 14 21 13.1 21 12C21 10.9 20.1 10 19 10ZM12 10C10.9 10 10 10.9 10 12C10 13.1 10.9 14 12 14C13.1 14 14 13.1 14 12C14 10.9 13.1 10 12 10Z" fill="currentColor"/>
        </svg>
      </div>
    </div>
  );
};

